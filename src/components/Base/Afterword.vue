      </p>
      <p>
        では、果たしてバロウズのその結果はどうだったのでしょうか？<br>
        その答えの一つとして、『裸のランチ』からとある会話文を引用したいと思います。
      </p>
      <p>
        ==========<br>
        「立てよ、ガーディ、土地の連中に敬意を示すんだ」<br>
        「そうすべきでしょうね」<br>
        「ねえきみ、ぼくは実に驚くべき発明に従事しているよ……<u>いく</u>と、とたんに消えてしまう少年なんだ、木の葉が燃えているにおいと、遠い汽車の汽笛の音を残してね」<br>
