      <h2>1. カットアップ</h2>
      <p>
        まず「カットアップ」とは何ぞや？　というお話です。
      </p>
      <p>
        カットアップとは、<u>既存の文章をハサミ等でばらばらに分解し、それらをランダムにつなぎ合わせて新しい文章を創り出すという創作手法</u>です。<br>
        時々、即興で組み合わせた言葉を書き換えることもありますが、基本的には上記の通りで、かなり偶発性を重視した創作手法であることに変わりありません。
      </p>
      <p>
        私がカットアップに注目した理由の１つに、「言語の不自由さからの開放」という点が挙げられます。
      </p><br>
      <h3>ウィトゲンシュタインとカットアップ</h3>
      <p>
        普段、我々は物事を考えるとき、言語を使用します。<br>
        オーストリアの哲学者 ルートヴィヒ・ウィトゲンシュタインは、著書『論理哲学論考』において、「思考の限界とは、言語の限界である」ということを本の中で指摘しています。
      </p>
      <p>
        ウィトゲンシュタインはその論証の中で、言葉同士を組み合わせが意味として成立するかを真理値（真：Ture、偽：False）によって判定するという操作を行っています（しばしばウィトゲンシュタインは、言葉の限界を越えた先を「ナンセンス」と呼びます）。<br>
        論理において偶然というものは存在しないため、ありとあらゆる言葉の組み合わせがすべて同等のものとして扱われます。
      </p>
      <p>
        論理哲学論考における基本的な関心は、「人間はどこまで物事を考えられるか」であるため、個々の言葉の組み合わせに関する具体的な可能性の値について検討する必要はありません。<br>
        ただ文章の意味が成立するか否かを知れればよいからです。
      </p>
      <p>
        しかし確実に言えることとして、<u>言葉同士の結びつきには明らかな偏りが存在します</u>。
      </p>
      <p>
        例えば、「月」という言葉を考えてみましょう。<br>
        日本語において「月」という言葉が出てくる文脈というのは、「夜」という言葉とセットであることが非常に多いです（例：今日の夜は月が見える）。<br>
        逆に、「月」という言葉と、今私がこの文章を書きながら片手間で食べている「ベ〇ー〇ターラー〇ン」との結びつきというのは、残念ながら「夜」という言葉と比べるとかなり低いと言わざるを得ません（例：月にベ〇ー〇ターラー〇ンを落とす）。
      </p>
      <p>
        もっと一般的な例を挙げるとするならば、ある言葉と組み合わされて使用される確率が高い言葉を示す「共起語・連語」なんかはまさにそれです。<br>
        こういった言葉が存在することからも、言葉同士の結びつきには偏りがあることがお分かりになるかと思います。
      </p>
      <p>
        この偏りを意識的に把握したり、あるいは破壊するといったことは、通常困難です。<br>
        ですが、これを効果的に破壊する方法を、我々は既に知っています。
      </p>
      <p>
        そう。<br>
